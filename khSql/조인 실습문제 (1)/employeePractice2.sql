SELECT EMPLOYEE.EMP_NAME "사원명",
JOB.JOB_NAME "직급명",
EMPLOYEE.DEPT_CODE "부서코드", 
DEPARTMENT.DEPT_TITLE "부서명"
FROM EMPLOYEE
