SELECT E.EMP_NAME, E.EMP_NO, D.DEPT_TITLE, A.JOB_NAME
FROM EMPLOYEE E
JOIN DEPARTMENT D
ON(E.DEPT_CODE=D.DEPT_ID)
JOIN JOB A
ON(E.JOB_CODE= A.JOB_CODE)
WHERE EMP_NO LIKE '7_____-2%' AND
EMP_NAME LIKE '전%'
;



