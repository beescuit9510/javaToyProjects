package kh.java.vo;

public class Silver {
	
	private String grade;
	private String name;
	private String point;
	
	
	public Silver() {
		
	}
	
	public Silver(String grade, String name, String point) {
		this.grade = grade;
		this.name = name;
		this.point = point;
	}
	public String getGrade() {
		return grade;
	}
	public void setGrade(String grade) {
		this.grade = grade;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getPoint() {
		return point;
	}
	public void setPoint(String point) {
		this.point = point;
	}
	
	
	
}
